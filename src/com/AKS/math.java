package com.AKS;

public class math {
    public static void main(String[] args){
        //max of two function
        System.out.println(Math.max(5,6));
        //min of two function
        System.out.println(Math.min(5,6));

        //random function
        System.out.println(Math.random());  //gives a random number of range 0.0 to 1.0
        System.out.println((int)(Math.random()*100)); // we will get random number from 0 to 100


    }
}
